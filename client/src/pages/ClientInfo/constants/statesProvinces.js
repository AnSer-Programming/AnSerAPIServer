// Flat list for simple dropdown
export const STATES_PROVINCES_FLAT = [
  ...US_STATES,
  ...CANADIAN_PROVINCES,
];

// Get state/province name by code
export const getStateProvinceName = (code) => {
  const found = STATES_PROVINCES_FLAT.find(
    (item) => item.code.toLowerCase() === (code || '').toLowerCase()
  );
  return found ? found.name : code;
};

// Check if code is valid
export const isValidStateProvince = (code) => {
  if (!code) return true; // Empty is valid (use required validation separately)
  return STATES_PROVINCES_FLAT.some(
    (item) => item.code.toLowerCase() === code.toLowerCase()
  );
};

